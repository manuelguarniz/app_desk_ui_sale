    @Override
    public void update(String action, Object data) {
        if ("update".equalsIgnoreCase(action)) {
            ViewProfileDTO dto = (ViewProfileDTO) data;
            lblUserProfile.setToolTipText(dto.getUsername());
//            lblUserProfile.setText(Util.ellipsis(dto.getUsername(), 10));
            lblUserProfile.setText(dto.getUsername());
            if (dto.getLastConection() == null) {
                dto.setLastConection(LocalDateTime.now());
            }
            String date = Util.formatDate(dto.getLastConection());
            lblLastConnection.setToolTipText(date);
            lblLastConnection.setText(date);
        }
    }

